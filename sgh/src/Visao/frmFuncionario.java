    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        
        if (txtNome.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Favor inserir um NOME");
            txtNome.requestFocus();
            return;
        }
        if (txtCPF.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Favor inserir o CPF");
            txtCPF.requestFocus();
            return;
        }
        
        if (txtRG.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Favor inserir o RG");
            txtRG.requestFocus();
            return;
        }
        

        if (txtEndereco.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Favor inserir um ENDERE√áO");
            txtEndereco.requestFocus();
            return;
        }
        
        if (txtsalario.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Favor inserir o SAL√ÅRIO");
            txtsalario.requestFocus();
            return;
        }
        if (txtLogin.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Favor inserir um LOGIN");
            txtLogin.requestFocus();
            return;
        }
        if (txtSenha.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Favor inserir uma SENHA");
            txtSenha.requestFocus();
            return;
        }
        
        

        Vfuncionario dts = new Vfuncionario();
        Ffuncionario func = new Ffuncionario();

        dts.setNome(txtNome.getText());

        dts.setCpf(txtCPF.getText());
        dts.setRg(txtRG.getText());
        
        dts.setEndereco(txtEndereco.getText());
        dts.setBairro(txtBairro.getText());
        dts.setCidade(txtCidade.getText());
        int seleccionado = cbUF.getSelectedIndex();
        dts.setUf((String) cbUF.getItemAt(seleccionado));
        dts.setCep(txtCEP.getText());
        
        dts.setEmail(txtEmail.getText());
        dts.setTelefone(txtTelefone.getText());
        
        
        dts.setSalario(Double.parseDouble(txtsalario.getText()));
        seleccionado=cbxAcesso.getSelectedIndex();
        dts.setAcesso((String)cbxAcesso.getItemAt(seleccionado));
        dts.setLogin(txtLogin.getText());
        dts.setPassword(txtSenha.getText());
        
        seleccionado=cbxStatus.getSelectedIndex();
        dts.setStatus((String)cbxStatus.getItemAt(seleccionado));
        
        if (acao.equals("salvar")) {
            if (func.insertar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "Funcionario cadastrado com sucesso!");
                mostrar("");
                desabiliar();

            }

        }
        else if (acao.equals("editar")){
            dts.setIdpessoa(Integer.parseInt(txtIdpessoa.getText()));

            if (func.editar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "Cadastro Editado com sucesso!");
                mostrar("");
                desabiliar();
            }
        }
                  
    }//GEN-LAST:event_btnSalvarActionPerformed

