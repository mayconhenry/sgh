    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (txtNome.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Favor inserir o nome do Cliente!");
            txtNome.requestFocus();
            return;
        }
        if (txtCPF.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Favor inserir o CPF!");
            txtCPF.requestFocus();
            return;
        }
        if (txtRG.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Favor inserir o RG");
            txtRG.requestFocus();
            return;
        }

        if (txtEndereco.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Favor inserir um Endereço!");
            txtEndereco.requestFocus();
            return;
        }
        if (txtBairro.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Campo obrigatorio!");
            txtBairro.requestFocus();
            return;
        }
        if (txtCidade.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Campo obrigatorio!");
            txtCidade.requestFocus();
            return;
        }
        if (txtCEP.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Campo obrigatorio!");
            txtCEP.requestFocus();
            return;
        }
        if (txtCodigo.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Campo obrigatorio!");
            txtCodigo.requestFocus();
            return;
        }
        vCliente dts = new vCliente();
        Fcliente func = new Fcliente();



        dts.setNome(txtNome.getText());
        dts.setCpf(txtCPF.getText());
        dts.setRg(txtRG.getText());
        dts.setEndereco(txtEndereco.getText());
        dts.setBairro(txtBairro.getText());
        dts.setCidade(txtCidade.getText());
        int seleccionado = cbUF.getSelectedIndex();
        dts.setUf((String) cbUF.getItemAt(seleccionado));
        dts.setCep(txtCEP.getText());
        dts.setEmail(txtEmail.getText());
        dts.setTelefone(txtTelefone.getText());
        dts.setCodigo_cliente(txtCodigo.getText());
        


        if (acao.equals("salvar")) {
            if (func.insertar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "Cliente cadastrado com sucesso!");
                mostrar("");
                desabiliar();

            }
        } else if (acao.equals("editar")) {
            dts.setIdpessoa(Integer.parseInt(txtIdpessoa.getText()));

            if (func.editar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "Cleinte Editado com sucesso!");
                mostrar("");
                desabiliar();
            }
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

